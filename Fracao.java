public class Fracao {

    public double Add(int num1, int num2 ){
        return num1 + num2;
    }
    public double Sub(int num1, int num2){
        return num1 - num2;
    }
    public double Mult(int num1, int num2){
        return num1 * num2;
    }
    public double Div(int num1, int num2){
        return num1/num2;
    }

}